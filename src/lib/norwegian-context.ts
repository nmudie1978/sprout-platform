export const NORWEGIAN_LABOR_CONTEXT = `
## NORWEGIAN YOUTH EMPLOYMENT LAW

### Age-Based Work Restrictions (Arbeidsmiljøloven)

**Ages 13-14:**
- Only "light work" permitted (no physical strain, no dangerous equipment)
- Maximum 2 hours on school days
- Maximum 12 hours per week during school term
- Examples: delivering newspapers, light garden work

**Ages 15-17:**
- Can work up to 8 hours per day
- Maximum 40 hours per week
- Cannot work between 23:00-06:00 (night work prohibited)
- Cannot work with dangerous machinery, chemicals, or heavy lifting
- Must have at least 12 hours continuous rest per day
- Work must not interfere with schooling
- Examples: retail, café work, grocery stores, babysitting

**Ages 18+:**
- Full working rights as an adult
- Can work night shifts
- Can operate machinery
- Can serve alcohol (requires course)
- Can drive company vehicles

### Required Documents
- **Skattekort (tax card)**: Required before starting any job. Get it from Skatteetaten.
- **Politiattest (police certificate)**: Required for work with children, elderly, or in healthcare
- **Bankkonto**: You need a Norwegian bank account to receive wages

### Minimum Wage in Norway
Norway has NO statutory minimum wage, BUT:
- Most industries have tariff agreements (tarifflønn) setting minimum rates
- Typical youth rates: 150-180 NOK/hour for unskilled work
- Retail tariff: ~160-175 NOK/hour
- Fast food: ~150-170 NOK/hour
- Tips: Some jobs (café, restaurant) may include tips on top of hourly rate
`;

export const NORWEGIAN_EDUCATION_CONTEXT = `
## NORWEGIAN EDUCATION SYSTEM

### Secondary Education (Videregående skole, ages 16-19)

**Studiespesialisering (General Studies):**
- Prepares for university/høgskole
- 3 years
- Leads to "generell studiekompetanse"

**Yrkesfag (Vocational Education):**
- 2 years in school + 2 years as apprentice (lærling)
- Leads to "fagbrev" (trade certificate)
- Popular paths: electrician, plumber, carpenter, healthcare worker, IT
- Many employers prefer fagbrev over university for practical jobs

### Apprenticeship (Lærling)
- Paid position while learning a trade
- Typical salary: 30-50% of regular wage (increases each year)
- Leads to fagbrev after completing training and exam (fagprøve)
- Highly respected in Norway

### Higher Education
- **Høgskole**: Bachelor's degrees (3 years), more practical focus
- **Universitet**: Bachelor's (3y), Master's (2y), PhD programs
- **Fagskoler**: 1-2 year vocational higher education

### Alternative Paths
- **Folkehøgskole**: 1-year experience schools (no grades, no exams, personal development)
- **Military service (førstegangstjeneste)**: 12-19 months, looks good on CV
- **Gap year with work**: Common and accepted in Norway
`;

export const NORWEGIAN_JOB_MARKET_CONTEXT = `
## NORWEGIAN JOB MARKET FOR YOUTH

### Seasonal Patterns
**Summer (June-August):**
- Peak hiring season for youth
- Tourism, outdoor events, festivals, ice cream shops
- Many apply in March-April for summer positions
- "Sommerjobb" applications are competitive

**Winter (November-March):**
- Snow clearing in high demand (can earn 100-200 NOK/hour)
- Ski resorts and mountain tourism
- Christmas retail rush (October-December)
- Indoor jobs more available

**Year-round opportunities:**
- Grocery stores (always hiring)
- Fast food (high turnover = frequent openings)
- Delivery services (Foodora, Wolt)
- Babysitting and pet care

### Where to Find Jobs
1. **Finn.no/jobb** - Norway's main job portal
2. **Nav.no** - Government employment service
3. **Direct applications** - Walk into stores/restaurants
4. **Network** - Ask family, neighbors, teachers
5. **Social media** - Local Facebook groups for "småjobber"
6. **Staffing agencies** - Adecco, Manpower, Jobzone for temporary work

### Application Tips for Norwegian Employers
- Keep your CV to 1 page
- Include "personnummer" (personal ID number) or birth date
- List references (referanser) - very important in Norway
- Norwegian employers value: reliability, punctuality, honesty
- Follow up applications with a phone call or visit
- Be direct but polite in communication
`;

